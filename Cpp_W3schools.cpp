#include <iostream>
using namespace std;

int main()
{
    int myNum = 1000;
    cout << "myNum = " << myNum << "\n";

    float myFloat = 5.43210123;
    cout << "myFloat = " << myFloat << "\n";

    double myDouble = 19.98765432;
    cout << "myDouble = " << myDouble << "\n";

    // Scientific Numbers
    float f1 = 35e3;
    double d1 = 12E4;
    cout << "f1 = " << f1 << endl;
    cout << "d1 = " << d1;

    return 0;
}
